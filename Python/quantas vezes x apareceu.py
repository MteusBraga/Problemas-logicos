'''Descrição Leia 10 números inteiros.

Depois leia mais um número inteiro x.

Sua missão é imprimir quantas vezes x apareceu entre os 10 primeiros inteiros lidos.'''

lista=[]
cont=0
for i in range():
    lista.append(int(input()))
num=int(input())
for i in range(10):
    if lista[i] == num:
        cont+=1
print(cont)
